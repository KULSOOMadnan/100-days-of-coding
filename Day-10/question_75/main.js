"use strict";
function compoundOperators() {
    let k = 8;
    console.log(`Initial value of k "${k}"`);
    // add  compound operator
    k += 2;
    console.log(`After Addition "${k}"`);
    // subtraction compound operator
    k -= 1;
    console.log(`After Subtraction "${k}"`);
    // mutliply compound operator
    k *= 2;
    console.log(`After Multiplication "${k}"`);
    // divide compond operator 
    k /= 2;
    console.log(`After Division "${k}"`);
}
compoundOperators();
